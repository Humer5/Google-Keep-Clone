function navigateTo(feature) {
    if (feature === 'crud') {
        window.location.href = 'CRUD.html'; // Replace with the CRUD branch's main file
    } else if (feature === 'todo') {
        window.location.href = 'ToDo.html'; // Replace with the To-Do branch's main file
    } else if (feature === 'images') {
        window.location.href = 'ImageNotes.html'; // Replace with the Image branch's main file
    }
}
